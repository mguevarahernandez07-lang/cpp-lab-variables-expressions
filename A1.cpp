#include <iostream>
using namespace std;

int main() {
    int x = 10, y = 5, z = 2;

    cout << "Expression 1: x + y * z = " << x + y * z << endl;
    cout << "Expression 2: (x + y) * z = " << (x + y) * z << endl;
    cout << "Expression 3: x - y - z = " << x - y - z << endl;
    cout << "Expression 4: x / y * z = " << x / y * z << endl;

    return 0;
}
